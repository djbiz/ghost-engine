#!/usr/bin/env node
/**
 * Ghost Engine - Proof Loop Engine
 * Every close -> case study -> content -> ads -> more closes
 * Run: node proof-loop.js trigger <name> <details...>
 */

const fs = require("fs");
const path = require("path");

const BASE = "/home/workspace/Ghost-Monitization-Engine";
const PROOF_DIR = BASE + "/proof";
const CLOSE_LOG = BASE + "/scripts/close-log.jsonl";
const LESSONS = BASE + "/lessons-learned.md";
const MEMORY = BASE + "/MEMORY.md";

if (!fs.existsSync(PROOF_DIR)) fs.mkdirSync(PROOF_DIR, { recursive: true });

function triggerProofLoop(clientData) {
  const id = Date.now();
  const { name, platform, followers, tier, investment, timeline, result, quote } = clientData;

  console.log("\n[PROOF LOOP] Triggered for: " + (name || "Client"));
  console.log("  Tier: " + tier + " | Investment: " + investment);
  console.log("  Result: " + result);

  // 1. Generate case study
  const caseStudy = generateCaseStudy(clientData, id);
  writeFile(PROOF_DIR + "/" + id + "-case-study.md", caseStudy);
  console.log("  [OK] Case study created");

  // 2. Generate 3 authority posts
  const posts = generateAuthorityPosts(clientData, id);
  writeFile(PROOF_DIR + "/" + id + "-authority-post-1.md", posts[0]);
  writeFile(PROOF_DIR + "/" + id + "-authority-post-2.md", posts[1]);
  writeFile(PROOF_DIR + "/" + id + "-authority-post-3.md", posts[2]);
  console.log("  [OK] 3 authority posts created");

  // 3. Generate breakdown
  const breakdown = generateBreakdown(clientData, id);
  writeFile(PROOF_DIR + "/" + id + "-breakdown.md", breakdown);
  console.log("  [OK] Breakdown created");

  // 4. Log to close log
  const logEntry = JSON.stringify({
    ts: new Date().toISOString(),
    type: "proof_loop_triggered",
    client: name,
    platform,
    followers,
    tier,
    investment,
    result,
    quote,
    files: [
      id + "-case-study.md",
      id + "-authority-post-1.md",
      id + "-authority-post-2.md",
      id + "-authority-post-3.md",
      id + "-breakdown.md"
    ]
  });
  fs.appendFileSync(CLOSE_LOG, logEntry + "\n", "utf8");
  console.log("  [OK] Close log updated");

  // 5. Update MEMORY.md
  updateMemory(clientData);
  console.log("  [OK] MEMORY.md updated");

  // 6. Update lessons-learned.md
  updateLessons(clientData);
  console.log("  [OK] lessons-learned.md updated");

  console.log("\n[PROOF LOOP] All files in: " + PROOF_DIR);
  console.log("\nNEXT: Post authority-post-1 TODAY. Use breakdown as carousel this week.");
}

