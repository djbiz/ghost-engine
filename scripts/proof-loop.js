function updateMemory(d) {
  const entry = `
## Win: ` + (d.name || "Client") + `
- **Date:** ` + new Date().toISOString().split("T")[0] + `
- **Platform:** ` + (d.platform || "Unknown") + `
- **Tier:** ` + (d.tier || "Quick Flip") + `
- **Result:** ` + (d.result || "Win logged") + `
- **Key insight:** ` + (d.insight || "See lessons-learned.md") + ``;

  if (fs.existsSync(MEMORY)) {
    fs.appendFileSync(MEMORY, entry, "utf8");
  }
}

function updateLessons(d) {
  const entry = `
## Close: ` + (d.name || "Client") + `
- **Date:** ` + new Date().toISOString().split("T")[0] + `
- **Tier:** ` + (d.tier || "Quick Flip") + `
- **What worked:** ` + (d.what_worked || "See case study") + `
- **What to remember:** ` + (d.lesson || "Log this win.") + ``;

  if (fs.existsSync(LESSONS)) {
    fs.appendFileSync(LESSONS, entry, "utf8");
  }
}

function writeFile(path, content) {
  fs.writeFileSync(path, content, "utf8");
}

function showProofs() {
  if (!fs.existsSync(PROOF_DIR)) { console.log("No proofs yet."); return; }
  const files = fs.readdirSync(PROOF_DIR).filter(f => f.endsWith(".md")).sort().reverse();
  console.log("\n===== PROOF FILES =====");
  files.forEach(f => {
    const stats = fs.statSync(PROOF_DIR + "/" + f);
    console.log("  " + f + " (" + Math.round(stats.size / 1024) + "KB)");
  });
}

const cmd = process.argv[2];
if (cmd === "trigger") {
  const args = process.argv.slice(3);
  triggerProofLoop({
    name: args[0] || "Client",
    platform: args[1] || "TikTok",
    followers: args[2] || "10K",
    tier: args[3] || "Quick Flip",
    investment: args[4] || "$990",
    timeline: args[5] || "48 hours",
    result: args[6] || "Revenue installed",
    quote: args[7] || "This was exactly what I needed."
  });
} else if (cmd === "list") {
  showProofs();
} else {
  console.log("Usage:");
  console.log("  node proof-loop.js trigger <name> <platform> <followers> <tier> <investment> <timeline> <result> [quote]");
  console.log("  node proof-loop.js list");
  console.log("\nExample:");
  console.log("  node proof-loop.js trigger \"Alex\" \"TikTok\" \"45K\" \"Full Engine\" \"$4970\" \"2 weeks\" \"First $10K month\" \"I finally have a system.\"");
}
